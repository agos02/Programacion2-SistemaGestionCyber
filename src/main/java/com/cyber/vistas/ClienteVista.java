    //Acción del botón Guardar
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtNombre.getText().trim();
        String dni = txtDni.getText().trim();
        String telefono = txtTelefono.getText().trim();

        // Validaciones básicas
        if (nombre.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar un nombre.");
            return;
        }
        if (dni.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar un DNI.");
            return;
        }
        if (!dni.matches("\\d{8}")) {
            javax.swing.JOptionPane.showMessageDialog(this, "El DNI debe tener exactamente 8 dígitos.");
            return;
        }
        if (controlador.existeDni(dni)) {
            javax.swing.JOptionPane.showMessageDialog(
            this,
            "Ya existe un cliente con ese DNI.");
            return;
        }
        
        if (telefono.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar un teléfono.");
            return;
        }
        if (!telefono.matches("\\d+")) {
            javax.swing.JOptionPane.showMessageDialog(this, "El teléfono solo puede contener números.");
            return;
        }
        if (telefono.length() < 10) { //Validar longitud mínima del teléfono
            javax.swing.JOptionPane.showMessageDialog(this, "El teléfono debe tener al menos 10 dígitos.");
            return;
        }
        if (telefono.length() > 12) { ////Validar longitud manima del teléfono
        javax.swing.JOptionPane.showMessageDialog(this, "El teléfono no puede tener más de 12 dígitos.");
            return;
        }

        // Guardar cliente
        boolean guardado = controlador.guardarCliente(nombre, dni, telefono);

        if (guardado) {
            javax.swing.JOptionPane.showMessageDialog(this, "Cliente guardado correctamente.");
            cargarTabla();
            txtNombre.setText("");
            txtDni.setText("");
            txtTelefono.setText("");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No se pudo guardar el cliente.");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    
    //Acción al hacer clic en la tabla (selección de cliente)
    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        int fila = tblClientes.getSelectedRow();

        if (fila != -1) {

         idClienteSeleccionado =
            Integer.parseInt(
                    tblClientes.getValueAt(fila, 0).toString()
            );

         txtNombre.setText(
            tblClientes.getValueAt(fila, 1).toString()
         );

        txtDni.setText(
            tblClientes.getValueAt(fila, 2).toString()
         );

        txtTelefono.setText(
            tblClientes.getValueAt(fila, 3).toString()
    );
}
    }//GEN-LAST:event_tblClientesMouseClicked
    
    
    //Acción del botón Modificar
    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
         if (idClienteSeleccionado == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un cliente de la tabla.");
            return;
        }

        String nombre = txtNombre.getText().trim();
        String dni = txtDni.getText().trim();
        String telefono = txtTelefono.getText().trim();

        boolean modificado = controlador.modificarCliente(idClienteSeleccionado, nombre, dni, telefono);

        if (modificado) {
            javax.swing.JOptionPane.showMessageDialog(this, "Cliente modificado correctamente.");
            cargarTabla();
            txtNombre.setText("");
            txtDni.setText("");
            txtTelefono.setText("");
            idClienteSeleccionado = -1;
            tblClientes.clearSelection();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No se pudo modificar el cliente.");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

