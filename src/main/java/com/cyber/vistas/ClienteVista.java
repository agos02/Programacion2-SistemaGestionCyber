    //Acción del botón Eliminar:   
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
         if (idClienteSeleccionado == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un cliente.");
            return;
        }

        int opcion = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "¿Desea eliminar el cliente seleccionado?",
                "Confirmar eliminación",
                javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (opcion == javax.swing.JOptionPane.YES_OPTION) {
            boolean eliminado = controlador.eliminarCliente(idClienteSeleccionado);

            if (eliminado) {
                javax.swing.JOptionPane.showMessageDialog(this, "Cliente eliminado correctamente.");
                cargarTabla();
                txtNombre.setText("");
                txtDni.setText("");
                txtTelefono.setText("");
                idClienteSeleccionado = -1;
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "No se pudo eliminar el cliente.");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    
